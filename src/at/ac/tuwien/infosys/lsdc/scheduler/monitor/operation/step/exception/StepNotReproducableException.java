package at.ac.tuwien.infosys.lsdc.scheduler.monitor.operation.step.exception;

public class StepNotReproducableException extends Exception {
	private static final long serialVersionUID = 1112510487830333851L;

	public StepNotReproducableException() {
		super();
	}

	public StepNotReproducableException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public StepNotReproducableException(String arg0) {
		super(arg0);
	}

	public StepNotReproducableException(Throwable arg0) {
		super(arg0);
	}
	
	
}
